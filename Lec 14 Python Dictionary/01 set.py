# This is a list
# A list is a collection which is ordered and changeable, allow duplicate
# Lists are written with square brackets.
programmingLanguage = ["Python", "Java", "C++", "C#", "Javacsript", "SQL", "Java"]


# This is a tuple
# A tuple is a collection which is ordered and unchangeable, allow duplicate
# Tuples are written with parenthesis (round brackets).
Fruits = ("Apple", "Orange", "Grapes", "Kiwi", "Cherry")

# Dictionaries are used to store data values in key:value pairs.
# A dictionary is a collection which is ordered*, changeable and do not allow duplicates.
# As of Python version 3.7, dictionaries are ordered. In Python 3.6 and earlier, dictionaries are unordered.
# Dictionaries are written with curly brackets, and have keys and values

info = {
    "name": "Alex",
    "gender": "Male",
    "city": "New York",
    "age": 33,
    "grade": 3.3453,
    "fullTimeStudent": True
}

print(info)
print(type(info))

